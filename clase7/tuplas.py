tupla = (6, "noviembre", 2017) #Reconoce que es una TUPLA porque esta entre (parentesis)
print(tupla) #Tupla entera
print(tupla[0]) #Seleccionas el elemento que quieras
print(tupla[:2])#:2, el limite es el 2, o sea te trae los que está en la posición 0 y 1
#Una tupla es inmutable, es una constante mientras está en ejecución. Tiene más de segura.
